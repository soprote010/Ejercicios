document.write("Programa para el manejo de operadores");

//el operador de asignacion =
var x = 5;
var y;
y = 6;

document.write("Valor x: " +x+" <br/>");
document.write("Valor y: " +y+" <br/>");

//operador suma +
var z = x+ y;
document.write("Valor z: "+z+" <br/>");

//operador ++ incremento en 1
x++;
document.write("Valor x: "+x+ " <br/>");

//operador de decremento --
y--;
document.write("Valor y: "+y+" <br/>");

//el operador +=
//z=x+y;
x+=y;
document.write("Valor x: "+x+" <br/>");

//operador de concatenacion + si es alguno de los valores es tipo cadena
